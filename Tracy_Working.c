// Toggle partner variable DO NOT CHANGE
int togglePartner = 1;

/*------------------Dead zone filter------------------------*/



int filter(int input){
	if (input > 20 || input < -20){
		return input;
	}
	else {
		return 0;
	}
}

task toggleTask(){
	while(true){
		if(vexRT[Btn7U] == 1){

			if(togglePartner == 1){
				togglePartner = 0;
			} else {
				togglePartner = 1;
			}

			wait1Msec(700);
		}
	}
}
