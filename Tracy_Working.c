/*------------------Mobile Goal-----------------------------*/
task mogoLift() {
	while(true){
		//assigning lifting the mobile goal to the upper 8-pad button
		while(vexRT[Btn8UXmtr2] == 1 || vexRT[Btn8U] == 1){
			startMotor(mogo, 80);
		}
		stopMotor(mogo);
		//assigning dropping the mobile goal to the bottom 8-pad button
		while(vexRT[Btn8DXmtr2] == 1 || vexRT[Btn8D] == 1){
			startMotor(mogo, -80);
		}
		stopMotor(mogo);
	}
}

