//Wait for Press--------------------------------------------------
void waitForPress()
{
	while(true){
		if(nLCDButtons == rightButton || nLCDButtons == centerButton) {
			break;
		}
	}
	wait1Msec(5);
}
//----------------------------------------------------------------

//Wait for Release------------------------------------------------
void waitForRelease()
{
	while(nLCDButtons != 0){}
	wait1Msec(5);
}
