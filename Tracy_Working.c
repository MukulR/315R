/*------------------Arm and claw and boxlift-----------------------------*/

task arm() {
	while(true){
		//assigning lifting the arm to the upper 6-button pad.
		while(vexRT[Btn6U] == 1) {
			startMotor(leftArm, 80);
			startMotor(rightArm, -80);
		}
		stopMotor(leftArm);
		stopMotor(rightArm);
		//assigning dropping the arm to the bottom 6-buttom pad.
		while(vexRT[Btn6D] == 1){
			startMotor(leftArm, -80);
			startMotor(rightArm, 80);
		}
		stopMotor(leftArm);
		stopMotor(rightArm);
	}
}

//----------------------Arm Box----------------

task box() {
	while(true){
		//box down
		while(vexRT[Btn5U] == 1){
			startMotor(armBox, 127);
		}
		stopMotor(armBox);

		//box up
		while(vexRT[Btn5D] == 1){
			startMotor(armBox, -127);
		}
		stopMotor(armBox);
	}
}

task claw() {
	while(true){
		//claw open
		if(vexRT[Btn8R] == 1){
			stopMotor(armClaw);
			startMotor(armClaw, -127);
			wait1Msec(100);
			stopMotor(armClaw);
			startMotor(armClaw, -15);
		}
		// claw close
		if(vexRT[Btn8L] == 1){
			stopMotor(armClaw);
			startMotor(armClaw, 127);
			wait1Msec(300);
			stopMotor(armClaw);
			startMotor(armClaw, 15);
		}
		//abort the constant power
		if(vexRT[Btn7D] == 1 || vexRT[Btn7DXmtr2] == 1){
			stopMotor(armClaw);
		}
	}
}


