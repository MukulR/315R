/*------------------Mobile Goal-----------------------------*/
task mogoLift() {
	while(true){
		//assigning lifting the mobile goal to the upper 8-pad button
		while(vexRT[Btn8U] == 1 || vexRT[Btn8UXmtr2] == 1){
			startMotor(mogo, 80);
		}
		stopMotor(mogo);
		//assigning dropping the mobile goal to the bottom 8-pad button
		while(vexRT[Btn8D] == 1 || vexRT[Btn8DXmtr2]){
			startMotor(mogo, -80);
		}
		stopMotor(mogo);
	}
}
/*-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_-_*/



/*------------------Arm and claw and boxlift-----------------------------*/

task arm() {
	while(true){
		//assigning lifting the arm to the upper 6-button pad.
		while(vexRT[Btn6U] == 1) {
			startMotor(leftArm, 80);
			startMotor(rightArm, -80);
		}
		stopMotor(leftArm);
		stopMotor(rightArm);
		//assigning dropping the arm to the bottom 6-buttom pad.
		while(vexRT[Btn6D] == 1){
			startMotor(leftArm, -80);
			startMotor(rightArm, 80);
		}
		stopMotor(leftArm);
		stopMotor(rightArm);
	}
}

task box() {
	while(true){
		//box down
		while(vexRT[Btn5U] == 1){
			startMotor(armBox, 127);
		}
		stopMotor(armBox);

		//box up
		while(vexRT[Btn5D] == 1){
			startMotor(armBox, -127);
		}
		stopMotor(armBox);
	}
}

task claw() {
	while(true){
		//claw close
		while(vexRT[Btn8R] == 1){
			startMotor(armClaw, -80);
		}
		stopMotor(armClaw);
		// claw open
		while(vexRT[Btn8L] == 1){
			startMotor(armClaw, 80);
		}
		stopMotor(armClaw);
	}
}
