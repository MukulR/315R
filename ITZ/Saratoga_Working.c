void boxForward(int power){
		motor[armBox] = 1 * power;
}

void openClaw(int power){
	motor[armClaw] = -1 * power;
	//wait1Msec(750);
	wait1Msec(400);
	motor[armClaw] = 0;
}

void armUp(int power){
	motor[leftArm] = power;
	motor[rightArm] = -1 * power;
}

void armDown(int power){
	motor[leftArm] = -1 * power;
	motor[rightArm] = power;
}

void moveForwardAuton(int power){
	motor[driveLeft] = power;
	motor[driveRight] = -1 * power;
}

void moveBackwardAuton(int power){
	motor[driveLeft] = -1 * power;
	motor[driveRight] = power;
}

void turnRightAuton(int power){
	motor[driveRight] = power;
	motor[driveLeft] = power;
}

void turnLeftAuton(int power){
	motor[driveRight] = -1 * power;
	motor[driveLeft] = -1 * power;
}

void stopAllMotorsAuton(){
	motor[driveRight] = 0;
	motor[driveLeft] = 0;
	motor[armBox] = 0;
	motor[armClaw] = 0;
	motor[leftArm] = 0;
	motor[mogo] = 0;
	motor[rightArm] = 0;
}

void mogoDownAuton(){
	motor[mogo] = -127;
	wait1Msec(1200);
	motor[mogo] = 0;
}

void mogoUpAuton(){
	motor[mogo] = 127;
	wait1Msec(1200);
	motor[mogo] = 0;
}

void RL_OR_BR_MobileAuton(bool redLeft){
	// Lift arm
	// Bring the mogo down
	// Move forward to the mobile goal
	armUp(50);
	wait1Msec(250);
	mogoDownAuton();
	// mogoDown waits, no need to add additional wait here
	moveForwardAuton(127);
	wait1Msec(1600);
	stopAllMotorsAuton();

	// Pickup mobile goal
	// Drop the yellow cone on mobile goal
	mogoUpAuton();
	// mogoUp waits, no need to add additional wait here
	armDown(80);
	wait1Msec(1000);
	stopAllMotorsAuton();
	openClaw(40);
	armUp(80);
	wait1Msec(250);
	stopAllMotorsAuton();

	// If we are pursuing red left auton then the robot
	// has to turn left slightly otherwise if we are pursuing
	// blue right auton then the robot has to turn right slightly.
	if (redLeft) {
		turnLeftAuton(80);
	} else {
		turnRightAuton(80);
	}
	wait1Msec(100);
	stopAllMotorsAuton();

	//continue normal routine
	moveBackwardAuton(127);
	wait1Msec(2275);
	stopAllMotorsAuton();

	// Depending on whether we are pursuing red left or blue right
	// we have to either turn left or turn right so that the robot
	// can be facing the drop zone.
	if (redLeft) {
		turnLeftAuton(80);
	} else {
		turnRightAuton(80);
	}
	wait1Msec(900);
	stopAllMotorsAuton();

	// Lift the arm up
	// Move forward so that robot's front is well within 10 point zone
	// Drop the mobile goal in 20 point zone
	// Drive back a bit
	armUp(80);
	wait1Msec(400);
	stopAllMotorsAuton();
	moveForwardAuton(127);
	wait1Msec(1500);
	stopAllMotorsAuton();
	mogoDownAuton();
	moveBackwardAuton(127);
	wait1Msec(150);
	stopAllMotorsAuton();
	mogoUpAuton();
	armDown(80);
	wait1Msec(600);
	stopAllMotorsAuton();
	moveBackwardAuton(127);
	wait1Msec(750);
	stopAllMotorsAuton();
}

void staticGoalAuton(){
	//arm up
	armUp(40);
	wait1Msec(1600);
	stopAllMotorsAuton();
	mogoDownAuton();
	//move forward
	moveForwardAuton(80);
	wait1Msec(600);
	stopAllMotorsAuton();
	// waiting to stop cone from shaking
	wait1Msec(3000);
	boxForward(300);
	wait1Msec(600);
	stopAllMotorsAuton();
	openClaw(80);
	moveBackwardAuton(80);
	wait1Msec(500);
	// Done
	stopAllMotorsAuton();
}

task autonomous(){
	clearLCDLine(0);
	clearLCDLine(1);
	switch(autonomousOption) {
		case 0:
			displayLCDCenteredString(0, "RedL Mobile");
			RL_OR_BR_MobileAuton(true /* Red Left is True */);
			break;
		case 1:
			displayLCDCenteredString(0, "BlueR Mobile");
			RL_OR_BR_MobileAuton(false /* Red Left is not True */);
			break;
		case 2:
			displayLCDCenteredString(0, "Static Goal");
			staticGoalAuton();
			break;
		case 3:
			displayLCDCenteredString(0, "Disabled");
			// Disable auton, do nothing.
			break;
	}
}

