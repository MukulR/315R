/*------------------------Drive-----------------------------*/
task drive(){
	while (true){
		//forward backward
		int forward = filter(vexRT[Ch1]);
		//turning
		int turn = filter(vexRT[Ch3]);

		motor[driveRight] = (forward - turn);
		motor[driveLeft] = (forward + turn);
	}
}
